def split_and_resample_data(X, y):
    """Split data and apply SMOTE+ENN for handling class imbalance"""
    print("\n4. DATA SPLITTING AND RESAMPLING")

    # Split data into train and test sets
    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=0.3, random_state=42, stratify=y
    )

    X_train = X_train.to_numpy()
    X_test = X_test.to_numpy()
    y_train = y_train.to_numpy()
    y_test = y_test.to_numpy()

    print(f"Training set size: {X_train.shape[0]}")
    print(f"Test set size: {X_test.shape[0]}")
    print(f"Original training class distribution: {pd.Series(y_train).value_counts().to_dict()}")

    # SMOTEENN for handling class imbalance
    print("Applying SMOTE + ENN resampling...")
    sm = SMOTEENN(random_state=42)
    X_train_resampled, y_train_resampled = sm.fit_resample(X_train, y_train)

    print(f"Resampled training set size: {X_train_resampled.shape[0]}")
    print(f"Resampled class distribution: {pd.Series(y_train_resampled).value_counts().to_dict()}")

    return X_train_resampled, X_test, y_train_resampled, y_test

X_train, X_test, y_train, y_test = split_and_resample_data(X,y)

