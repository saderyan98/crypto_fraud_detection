# 5. MODEL IMPLEMENTATIONS

class FraudDetectionModels:
    """Class containing all ML models for fraud detection"""

    def __init__(self):
        self.models = {}
        self.predictions = {}
        self.results = {}

    def train_neural_network(self, X_train, y_train, X_test):
        """Neural Network (MLP) implementation"""
        print("\nTraining Neural Network...")

        model = Sequential([
            Dense(128, activation='relu', input_shape=(X_train.shape[1],)),
            Dropout(0.2),
            Dense(64, activation='relu'),
            Dropout(0.2),
            Dense(32, activation='relu'),
            Dropout(0.2),
            Dense(1, activation='sigmoid')
        ])

        model.compile(
            optimizer=Adam(learning_rate=0.001),
            loss='binary_crossentropy',
            metrics=['accuracy']
        )

        # Model training
        history = model.fit(
            X_train, y_train,
            epochs=50,
            batch_size=32,
            validation_split=0.2,
            verbose=0
        )

        # Predictions
        y_pred_proba = model.predict(X_test, verbose=0)
        y_pred = (y_pred_proba > 0.5).astype(int).flatten()

        self.models['Neural Network'] = model
        self.predictions['Neural Network'] = y_pred

        return y_pred

    def train_cnn(self, X_train, y_train, X_test):
        """Convolutional Neural Network implementation"""
        print("Training CNN...")

        # Reshape data for CNN (add sequence dimension)
        X_train_cnn = X_train.reshape(X_train.shape[0], X_train.shape[1], 1)
        X_test_cnn = X_test.reshape(X_test.shape[0], X_test.shape[1], 1)

        model = Sequential([
            Conv1D(64, 3, activation='relu', input_shape=(X_train.shape[1], 1)),
            MaxPooling1D(2),
            Conv1D(32, 3, activation='relu'),
            MaxPooling1D(2),
            Flatten(),
            Dense(64, activation='relu'),
            Dropout(0.2),
            Dense(1, activation='sigmoid')
        ])

        model.compile(
            optimizer=Adam(learning_rate=0.001),
            loss='binary_crossentropy',
            metrics=['accuracy']
        )

        # Train the model
        history = model.fit(
            X_train_cnn, y_train,
            epochs=50,
            batch_size=32,
            validation_split=0.2,
            verbose=0
        )

        # Make predictions
        y_pred_proba = model.predict(X_test_cnn, verbose=0)
        y_pred = (y_pred_proba > 0.5).astype(int).flatten()

        self.models['CNN'] = model
        self.predictions['CNN'] = y_pred

        return y_pred

    def train_decision_tree(self, X_train, y_train, X_test):
        """Decision Tree implementation"""
        print("Training Decision Tree...")

        model = DecisionTreeClassifier(
            max_depth=10,
            random_state=42,
            class_weight='balanced'
        )

        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)

        self.models['Decision Tree'] = model
        self.predictions['Decision Tree'] = y_pred

        return y_pred

    def train_random_forest(self, X_train, y_train, X_test):
        """Random Forest implementation with hyperparameter tuning"""
        print("Training Random Forest...")

        # Hyperparameter tuning
        param_distributions = {
            'n_estimators': [50, 100, 200],
            'max_depth': [10, 20, None],
            'min_samples_split': [2, 5, 10],
            'min_samples_leaf': [1, 2, 4]
        }

        rf = RandomForestClassifier(random_state=42, class_weight='balanced')

        # RandomizedSearchCV for hyperparameter tuning
        rf_random = RandomizedSearchCV(
            estimator=rf,
            param_distributions=param_distributions,
            n_iter=10,
            cv=3,
            verbose=0,
            random_state=42,
            n_jobs=-1
        )

        rf_random.fit(X_train, y_train)

        # Use best model
        model = rf_random.best_estimator_
        y_pred = model.predict(X_test)

        print(f"Best RF parameters: {rf_random.best_params_}")

        self.models['Random Forest'] = model
        self.predictions['Random Forest'] = y_pred

        return y_pred

    def train_gradient_boosting(self, X_train, y_train, X_test):
        """Gradient Boosting (XGBoost) implementation"""
        print("Training Gradient Boosting...")

        # Calculate scale_pos_weight for imbalanced data
        scale_pos_weight = (y_train == 0).sum() / (y_train == 1).sum()

        model = xgb.XGBClassifier(
            n_estimators=100,
            learning_rate=0.1,
            max_depth=6,
            scale_pos_weight=scale_pos_weight,
            random_state=42,
            eval_metric='logloss'
        )

        model.fit(X_train, y_train)
        y_pred = model.predict(X_test)

        self.models['Gradient Boosting'] = model
        self.predictions['Gradient Boosting'] = y_pred

        return y_pred

